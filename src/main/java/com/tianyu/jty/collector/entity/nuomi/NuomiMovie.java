public class NuomiMovie {
    private String uid;
    private String score;
    private String image;
    private String name;
    private String address;
    private String city;
    private String area;
    private String phone;
    private String shopHours;
    private String description;
    private Double baidu_longitude;
    private Double baidu_latitude;
    private Double distance;

    public NuomiMovie() {
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getShopHours() {
        return shopHours;
    }

    public void setShopHours(String shopHours) {
        this.shopHours = shopHours;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Double getBaidu_longitude() {
        return baidu_longitude;
    }

    public void setBaidu_longitude(Double baidu_longitude) {
        this.baidu_longitude = baidu_longitude;
    }

    public Double getBaidu_latitude() {
        return baidu_latitude;
    }

    public void setBaidu_latitude(Double baidu_latitude) {
        this.baidu_latitude = baidu_latitude;
    }

    public Double getDistance() {
        return distance;
    }

    public void setDistance(Double distance) {
        this.distance = distance;
    }
}
